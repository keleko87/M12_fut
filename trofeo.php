        <pre>
          ¿Cuándo hay que pasar por la sede del Trofeo?
            Hay que pasar dos veces por semana, de 20:00 a 21:00 h. Una, los lunes para poner y recoger los horarios de los partidos, así como ver en el tablón las sanciones para posibles reclamaciones. Dos, los viernes, para recoger la carpeta del encuentro, pagar las sanciones y recoger las fichas de los jugadores que cumplen sanción.

            Después del partido, ¿Hay que llevar la carpeta a la sede del Trofeo?
            SI. Hay de plazo desde la finalización del encuentro hasta el domingo a las 16:00 h.

            Durante el encuentro, ¿Los dos equipos pueden usar la misma equipación?
            NO. Según el Art. F-4 del Reglamento de Régimen Interno, el equipo visitante debe cambiarse la indumentaria. Si el pantalón del equipo visitante, tanto el de la equipación principal como el de la segunda equipación, coincide con el del local, la Organización dispone de un par de juegos de pantalones para prestarles a los equipos que lo necesiten.

            El campo/pabellón en el que voy a jugar como local no dispone de vestuarios. ¿Se puede jugar en él?
            NO. Según el Art. A-5 del Reglamento de Régimen Interno, es obligatorio que las instalaciones donde se vayan a desarrollar los encuentros dispongan de vestuarios.

            No tengo delegado/entrenador para el partido. ¿Qué puedo hacer?
            Dirigirse a la organización del Trofeo y pedirle que expidan una autorización para una persona que tu equipo determine, para que haga las funciones de delegado/entrenador durante el desarrollo de ese partido.

            Solamente se entregarán tres (3) autorizaciones por equipo y temporada.

            ¿Puedo inscribir en el Trofeo a jugadores federeados?
            SÍ y NO. Según el Art. G-2 del Reglamento de Régimen Interno,los jugadores inscritos en este Trofeo no pueden ser participantes en otra competición federada en el mismo deporte, es decir, un jugador federado en fútbol no puede inscribirse en ningún equipo de fútbol, pero sí en uno de fúbol sala, o al revés.

            ¿Puedo inscribir en el Trofeo a jugadores menores de edad?
            SI. Según el Art. G-1 del Reglamento de Régimen Interno, la edad para poder participar en el Trofeo es de 17 años cumplidos. No obstante se autoriza la inscripción de tres (3) jugadores por equipo con una edad mínima de 16 años. Todos ellos deben tener autorización paterna.

            ¿Un jugador puede hacer de delegado?
            NO. Puede tener ficha de jugador y de delegado, pero en el mismo partido no puede hacer las dos funciones a la vez. O figura de delegado o figura de jugador.

            ¿Un jugador puede hacer de entrenador?
            SI. Puede tener ficha de ambos puestos y hacer las dos funciones a la vez durante el desarrollo del partido.


          Como consecuencia de la cortedad de la competición, la organización puso en marcha la Copa de la Liga, ganada por Calvarrasa de Arriba, siendo Prosperidad, nuevamente, segundo clasificado y equipo más deportivo.

          En la quinta edición (Temporada 1991/92), se pone en marcha una novedad importante. Debido a la inscripción de 20 equipos, la organización decide dividir a los participantes en dos grupos de 10. Tras una liguilla regular, los cinco primeros de cada grupo disputarán un campeonato de lucha por el título y los cinco restantes de cada grupo originario jugarán una liguilla para establecer los puestos del 11 al 20. Quedando el Jesús Obrero como campeón.

          Después de este experimento, vuelve la competición a la fórmula clásica, una liga de 18 equipos.

          Al comienzo de la 7ª edición, temporada 1993/94, la dirección del Trofeo decide salir de la sede anterior, Bar Unión, e instalarse en la que sería sede social durante 7 temporadas, el "Restaurante Monsoni", propiedad de D. Primitivo Hernández Aparicio.

          En el mes de mayo del 94, finalizando la temporada, el Trofeo se ve salpicado por la desgracia. En un trágico accidente de automóvil fallece Sebas, delegado y colaborador del Trofeo.

          Las siguientes siete temporadas, el patrocinio del Trofeo está bajo la tutela de "Cárnicas Ance", hasta el 08-01-1995 que se incorpora como nuevo patrocinador "Seyprol Seguridad".

          Antes de finalizar la temporada 1995/96, Manuel Infante, presidente del Trofeo, anuncia su dimisión al frente del mismo, lo que hace que los equipos se movilicen y, por primera vez en la historia del Trofeo, los delegados y representantes de los equipos, tienen que tomar decisiones de relevancia. La primera y más trascendente de esas decisiones, es formar una junta directiva, nombrada por ellos mismos, entre personas relacionadas con el Trofeo. El empeño de esta junta directiva: mejorar la imagen del Trofeo.

          La 11ª edición, temporada 1997/98, comienza con un nuevo patrocinador: "Extintores Origen". Dicho patrocinio se mantiene hasta la temporada 14 en la que se incorpora un nuevo mecenas, Inmobiliaria Hernández.

